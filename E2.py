# Purpose: Demonstrate conversions between hexadecimal, decimal, binary and ASCII

# Convert hexadecimal to decimal
print("Hex 'FF' to decimal =", int('FF', 16))

# Convert decimal to hexadecimal
print("Decimal 255 to hex =", hex(255))

# Convert decimal to binary
print("Decimal 42 to binary =", bin(42))

# ASCII conversions
print("ASCII 65 to character =", chr(65))
print("Character 'A' to ASCII =", ord('A'))

# Exercise conversions
print("Binary 0b11110000 to decimal =", 0b11110000)
print("ASCII 'Z' to hex =", hex(ord('Z')))
